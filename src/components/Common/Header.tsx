export function Header() {
    const [openNav, setOpenNav] = React.useState(false);

    const handleWindowResize = () => typeof window !== 'undefined' && window.innerWidth >= 960 && setOpenNav(false);

    React.useEffect(() => {
        typeof window !== 'undefined' && window.addEventListener("resize", handleWindowResize);

        return () => {
            typeof window !== 'undefined' && window.removeEventListener("resize", handleWindowResize);
        };
    }, []);

    return (
        <Navbar variant="gradient"
            color="blue-gray"
            className="w-full my-2 from-blue-gray-900 to-blue-gray-800 px-4 py-3">
            <div className="flex items-center justify-between text-blue-gray-900">
                <Typography
                    as="a"
                    href="#"
                    variant="h6"
                    className="mr-4 cursor-pointer py-1.5 text-white"
                >
                    NEXTDEVBLOG
                </Typography>
                <div className="hidden lg:block">
                    <NavList />
                </div>
                <IconButton
                    variant="text"
                    className="ml-auto h-6 w-6 text-inherit hover:bg-transparent focus:bg-transparent active:bg-transparent lg:hidden"
                    ripple={false}
                    onClick={() => setOpenNav(!openNav)}
                >
                    {openNav ? (
                        <XMarkIcon className="h-6 w-6" strokeWidth={2} />
                    ) : (
                        <Bars3Icon className="h-6 w-6" strokeWidth={2} />
                    )}
                </IconButton>
            </div>
            <Collapse open={openNav}>
                <NavList />
            </Collapse>
        </Navbar>
    );
}